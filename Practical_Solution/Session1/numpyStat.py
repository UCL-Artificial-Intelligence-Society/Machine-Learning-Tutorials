import numpy as np

x = np.array([34, 54, 6, 3, 89, 120, 11, 44, 77], dtype=np.int32)

# TODO: minimum element of x
array1 = np.min(x)
print("minimum: ", array1)

# TODO: maximum element of x
array2 = np.max(x)
print("maximum: ", array2)

# TODO: Mean of x
array3 = np.mean(x)
print("Mean: ", array3)

# TODO: Median of x
array4 = np.median(x)
print("Median: ", array4)

# TODO: Variation of x
array5 = np.var(x)
print("Variation: ", array5)

# TODO: Standard Deviation of x
array6 = np.std(x)
print("Standard Deviation: ", array6)